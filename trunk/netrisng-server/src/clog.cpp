string cLog::itoa(int n)
{
	string result="";
	while (n!=0)
	{
		result=char(48+n%10)+result;
		n/=10;
	}
	
	return result;
}
//-------------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------------
cLog::cLog(char* filename) : logFile(filename) {
  fileStream.open(logFile, ios::app);
  if (!fileStream) std::cout << "cLog: Couldn't open file" << std::endl;
}
//-------------------------------------------------------------------------------------------------------------
//-------------------------------------------------------------------------------------------------------------
cLog::~cLog() { fileStream.close(); }
